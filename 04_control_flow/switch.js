const month ="jan"

switch(month){
    case "jan" : 
                console.log("January");
                break;
    case "feb" : 
                console.log("Feb");
                break;
    case "mar" : 
                console.log("Mar");
                break;
    case "Apr" : 
                console.log("Apr");
                break;
    default : 
                console.log("Default case match");
                break;
}